                <p class="text-center"> <strong >Elektrobit ESB Architecture</strong></p>

                <p>Fiorano delivered sophisticated message transformation logic,
                    customized content filtering and solid web service support
                    with XML compatibility. Being platform independent and
                    consisting of an extensive set of connectivity methods
                    enabling integration out of the box, it was an ideal choice for
                    Elektrobit. To accelerate scalability, Fiorano SOA offered
                    consistent reusability features allowing Elektrobit to use
                    components developed once across new integration flows,
                    so that servers could be quickly added without much rework
                    or performance trade-off.
                </p>
                <h2>Benefits</h2>
                <span style="color:#CC4400">1. Out of the box functionality: </span>
                <p>
                    Since Elektrobit's environment incorporated several
                    applications, there was a need for several connectivity
                    methods, message transformation, content filtering, xml
                    support and web service support, all of which were
                    provided by the Fiorano ESB. Testing was also a key
                    benefit as automated tests were generated for schemas
                    and data.</p>

                <span style="color:#CC4400">2. Flexibility: </span>
                <p>
                    The Fiorano ESB architecture allows live changes to be
                    made made to running business process without
                    interrupting any of the other services. Independent
                    applications as well as flows were modified on the go without having to re-design the whole process. This not
                    only decreased development time but also allowed
                    automated tests to be generated for schemas and
                    data flows.
                </p>
                <span style="color:#CC4400">3. Reusability: </span>
                <p>
                    Solutions once developed and tested were easily
                    re-used in new integration process flows. This became
                    a very important benefit for rapid application integration
                    as the efficiency of designing new flows increased
                    linearly. In Fiorano, most of the integration processes
                    were ready to use a few hours after the installation
                    process.
                </p>
                <span style="color:#CC4400">4. Minimal Training and Resources Required: </span>
                <p>
                    With Fiorano, there was no need for massive training or
                    a large number of senior consultants for application
                    integration. The Fiorano Studio enables a
                    drag-and-drop based approach to building integration
                    flows, hence product training required was minimal.
                    Currently only one experienced developer takes care of
                    over 40 live application integration processes.
                </p>
                <span style="color:#CC4400">5. Fast Time to Production &amp; responsiveness: </span>
                <p>
                    Even though they were working on tight project
                    schedule, Elektrobit found that Fiorano was very easy
                    to deploy and setup for internal use. It quickly
                    accommodated changes in business logic and adapted
                    the existing process to match these changes efficiently.
                </p>
                <span style="color:#CC4400">6. Product Support: </span>
                <p>
                    Due to a tight schedule and adoption of a new
                    technology, Elektrobit expected that there would be
                    heavy utilization of product support during the project
                    startup phase and further down the road in the overall
                    integration lifecycle. Elektrobit received extensive and
                    reliable support during implementation and creation of
                    application integration processes.
                </p>



