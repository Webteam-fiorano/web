                <h3 class="casestudyh3">Swiss Hospitality major deploys Fiorano Cloud ESB Integration Platform</h3>
                <h4>Customer Profile</h4>
                <p>As the world's first hotel school, the Ecole h&#244;teli&#232;re de Lausanne (EHL) is a leading institution that prides itself in preparing the leaders of tomorrow. EHL is an international, bilingual school, offering its programs in both English and French. It provides the solid management education of a top business school while basing its programs on its own unique educational philosophy: hospitality management as both a science and an art. </p>
                <h4>Business Problem</h4>
                <p>Over the past several years, EHL acquired substantial hardware and software infrastructure, with a data center hosting over 100 servers (both physical and virtual VMware instances), together with over 70 applications ranging from simple portlets to complex applications. The school's pillar applications include a Salesforce.com for CRM, Microsoft Dynamics AX 2010 as an ERP platform, academic and end-user portals, Microsoft Exchange for email and calendaring and Microsoft ActiveDirectory for Identity and Access Management.</p>
                <p>To serve an increasingly larger body of students, EHL needed to automate various business processes. For instance, the automated creation of leads and prospects from EHL's website, the creation of student profiles from Admission and Enrollment information, feeding data for the automatic creation of invoices for students based on varying criteria, automatic updates of Alumni profiles from the end-user portal to the CRM system, etc.</p>
                <p>Initial efforts focused on manual efforts with redundant logic spread across several point-to-point integrations. Batch loads made business processes difficult to document and track and reduced the visibility of information flow. Any change to a process required forced changes at multiple end-points, often performed manually, further reducing reuse potential and making business reporting even more difficult.</p>
                <p>EHL soon realized that it needed to invest in a quality integration platform to automate business processes, increase transactional visibility and gain agility by developing capabilities to incorporate new business processes seamlessly without disrupting existing systems.</p>
                <p>A search was initiated for a comprehensive integration platform and choices were narrowed down to Microsoft Biztalk, Mule (Open Source) and Fiorano.</p>
                <strong class="b">Key decision criteria included:</strong>
                <ul>
                    <li>Zero-coding composition</li>
                    <li>Rapid debugging and deployment of integration flows</li>
                    <li>Web based monitoring tools</li>
                    <li>Scalability and Affordability</li>
                </ul>
                <br/>
                <h4>Solution</h4>
                <div style="text-align:justify">After performing Proofs-of-concept with all three products based on the above criteria, Fiorano emerged as the clear winner. "We found that developing and deploying new processes, as well as modifying and redeploying existing processes, was much faster with Fiorano in comparison with BizTalk and Mule. Fiorano also had a lower learning curve, decreasing our time to deployment significantly", said Ganasekharan Thoppe, Application Engineer, EHL.</div>
                <p>The Fiorano ESB forms the integration layer moving data between the pillar applications. Fiorano "event processes" implement specific business processes, allowing EHL to automate operations incrementally. Testing was simplified with the help of the Fiorano Studio's sophisticated debugging capabilities. With Fiorano's peer-to-peer architecture EHL has managed to evolve it's ESB infrastructure as requirements have grown and the experience with the ESB solution has matured. In summary, Fiorano was chosen for the following reasons:
                <ul>
                    <li>Code-free/Model-driven approach using a flow designer GUI</li>
                    <li>Faster development and debugging of event processes</li>
